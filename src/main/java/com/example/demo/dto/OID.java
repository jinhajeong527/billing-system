package com.example.demo.dto;

public class OID {

    int oid;
    String oname;
    String otype;
    String ip;
    Integer core;
    int mcore;

    public int getOid() {
        return oid;
    }
    public void setOid(int oid) {
        this.oid = oid;
    }
    public String getOname() {
        return oname;
    }
    public void setOname(String oname) {
        this.oname = oname;
    }
    public String getOtype() {
        return otype;
    }
    public void setOtype(String otype) {
        this.otype = otype;
    }
    public String getIp() {
        return ip;
    }
    public void setIp(String ip) {
        this.ip = ip;
    }
    public Integer getCore() {
        return core;
    }
    public void setCore(Integer core) {
        this.core = core;
    }
    public int getMcore() {
        return mcore;
    }
    public void setMcore(int mcore) {
        this.mcore = mcore;
    }
    
}